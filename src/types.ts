export type UktiDefinitionItemVariables = [Record<string, unknown>]

export type UktiDefinitionItem = undefined | UktiDefinitionItemVariables

export type UktiDefinition = Record<string, UktiDefinitionItem | Record<string, UktiDefinitionItem>>

export type UktiTranslationData<Definition extends UktiDefinition> = {
  [P in keyof Definition]: Definition[P] extends UktiDefinitionItemVariables
    ? string
    : undefined extends Definition[P]
      ? string
      : Definition[P] extends Record<string, UktiDefinitionItem>
        ? Record<keyof Definition[P], string>
        : never
}

type PartialDeep<T> = {
  [P in keyof T]?: PartialDeep<T[P]>;
}

export type UktiTranslationDataPartial<Definition extends UktiDefinition> =
  PartialDeep<UktiTranslationData<Definition>>

export type UktiTranslation<Definition extends UktiDefinition, Regions extends string = UktiRegions> =
  UktiTranslationData<Definition> & {
    regions?: {
      [R in Regions]?: UktiTranslationDataPartial<Definition>
    }
  }

type UktiLanguageDefault = 'en'

export type UktiTranslations<Definition extends UktiDefinition, Languages extends UktiLanguages = UktiLanguages, LanguageDefault extends UktiLanguages = UktiLanguageDefault, Regions extends string = UktiRegions> =
  Partial<Record<Languages, UktiTranslation<Definition, Regions>>>
  & Record<LanguageDefault, UktiTranslation<Definition, Regions>>

export type UktiTranslate<Definition extends UktiDefinition> = {
  [P in keyof Definition]: Definition[P] extends Record<string, unknown>
    ? {
        [Q in keyof Definition[P]]: (...parameters: Definition[P][Q] extends UktiDefinitionItemVariables ? Definition[P][Q] : []) => string
      }
    : (...parameters: Definition[P] extends UktiDefinitionItemVariables ? Definition[P] : []) => string
}

export type UktiTranslator<Definition extends UktiDefinition, Languages extends UktiLanguages = UktiLanguages, Regions extends string = UktiRegions> =
  (language: Languages, region?: Regions) => UktiTranslate<Definition>
